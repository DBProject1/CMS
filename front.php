<FONT SIZE="5" COLOR="BLACK">A content management system (CMS) is a computer application that supports the creation and modification of digital content using a common user interface and thus usually supporting multiple users working in a collaborative environment.This system will eliminate the manual entry mechanism for writing content</FONT>
<br>
<br>
<FONT SIZE="6" COLOR="RED">  Two major component of CMS- <br></FONT>
<br>
<FONT SIZE="5" COLOR="BLACK"> a) A content management application (CMA) is the front-end user interface that allows a user, even with limited expertise, to add, modify and remove content from a Web site without the intervention of a webmaster.<br></FONT>
<br>
<FONT SIZE="5" COLOR="BLACK"> b) A content delivery application (CDA) compiles that information and updates the Web site.<br></FONT>
